// Function to get provinces
function getProvinces() {
    return Object.keys(philippineLocations);
}

// Function to get cities/municipalities for a province
function getCities(province) {
    if (philippineLocations[province]) {
        return Object.keys(philippineLocations[province]);
    }
    return [];
}

// Function to get barangays for a city/municipality in a province
function getBarangays(province, city) {
    if (philippineLocations[province] && philippineLocations[province][city]) {
        return philippineLocations[province][city];
    }
    return [];
}



